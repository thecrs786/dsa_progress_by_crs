class Solution {
public:
    void reverseString(vector<char>& s) {
        // Initialize two pointers: one at the start and one at the end
        int start = 0;
        int end = s.size() - 1;

        // Loop runs until the pointers cross
        while (start < end) {
            // Swap characters at start and end indices
            swap(s[start], s[end]);

            // Move both pointers toward the center
            start++;
            end--;
        }

        /*
        â–‘â–‘ WHY THIS WORKS:
        ðŸ”¸ We're using two pointers to mirror elements from both ends inward.
        ðŸ”¸ Each iteration swaps the ith element from the start with the ith element from the end.
        ðŸ”¸ Since we're modifying in-place and using no extra memory, it meets the problem's constraints.
        */
    }
};

